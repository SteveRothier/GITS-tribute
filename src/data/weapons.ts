// Types pour les armes
export type WeaponCategory = 'SMG' | 'AR' | 'SR' | 'SG' | 'MG'
export type WeaponType = 'primary' | 'secondary' | 'melee' | 'throwable'

type WeaponSkin = {
  label: string
  src: string
}

// Stats pour les armes primaires et secondaires
type PrimarySecondaryStats = {
  damage: number // 0-100
  accuracy: number // 0-100
  stability: number // 0-100
  fireRate: number // 0-100
  range: number // en mètres
  magazineSize: number // nombre de balles par chargeur
  totalAmmo: number // nombre total de balles
}

// Stats pour les armes jetables
type ThrowableStats = {
  damage: number
  explosionRadius: number // zone d'explosion en mètres
  detonationDelay: number // délai de détonation en secondes
}

// Stats pour les armes de mêlée
type MeleeStats = {
  damage: number
  fireRate: number // 0-100
  range: number // en mètres
}

type WeaponStats = PrimarySecondaryStats | ThrowableStats | MeleeStats

export type Weapon = {
  id: string
  name: string
  type: WeaponType
  category?: WeaponCategory // Seulement pour les armes principales
  price: number // Prix en GP
  isNew?: boolean
  image: string
  skins?: WeaponSkin[]
  stats: WeaponStats
  description?: string
  compatibleAttachments?: {
    sight?: string[] // IDs des sights compatibles, undefined ou [] = none
    aimAssist?: string[] // IDs des aim assists compatibles
    barrel?: string[] // IDs des barrels compatibles
    muzzle?: string[] // IDs des muzzles compatibles
    magazine?: string[] // IDs des magazines compatibles
    grip?: string[] // IDs des grips compatibles
    bipod?: string[] // IDs des bipods compatibles
  }
}

